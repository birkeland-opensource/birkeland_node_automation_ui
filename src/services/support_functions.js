export const extract_fee_earned_channel_opening_cost = (extract_fee_earned) =>{
  let return_data = {fee_earned : 0,channel_opening_cost : 0, total_forwarded_sats : 0  };
  for (var item in extract_fee_earned){
    if(extract_fee_earned[item]?.outgoing_info?.total_fee_mtokens){
      return_data["fee_earned"] +=extract_fee_earned[item]?.outgoing_info?.total_fee_mtokens;
    }
     
    if(extract_fee_earned[item]?.total_satoshis_sent){
      return_data["total_forwarded_sats"] +=Number(extract_fee_earned[item]?.total_satoshis_sent);
    }
      if(extract_fee_earned[item]?.transaction_fee){
        return_data["channel_opening_cost"] +=extract_fee_earned[item]?.transaction_fee;
      }
   
  }
  return return_data;
}

export const get_fee_earned_from_forwards = (forwards) =>{
    try{
        let return_data = {amount_forwarded:0,fee_earned : 0, total_forwards : forwards.length, average_fee_earned : 0};
        for (var item in forwards){
           
            if(forwards[item]?.fee){
                return_data["fee_earned"] +=Number(forwards[item]?.fee);
            }
             if(forwards[item]?.amt_out){
                return_data["amount_forwarded"] +=Number(forwards[item]?.amt_out);
            }
        }
        return_data["average_fee_earned"] = return_data["fee_earned"]/return_data["total_forwards"];
        return return_data;
    }
    catch(err){
        
    }
}


export const getDateRange = (range) => {
    const endDate = new Date();
    const startDate = new Date();
  
    // Subtract 7 days from the current date
    startDate.setDate(startDate.getDate() - range);
  
    return {
      start_date: startDate.toISOString().split('T')[0],
      end_date: endDate.toISOString().split('T')[0],
    };
  }

 